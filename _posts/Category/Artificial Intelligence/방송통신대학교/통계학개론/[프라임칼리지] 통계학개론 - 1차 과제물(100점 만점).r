# 문제2. [3강. 데이터 수치요약]
# 어느 도시의 대기오염 정도를 파악하기 위해 한 달 동안 매일 오후 2시에 대기 중 이산화탄소 농도를 측정하였다. 아래의 표는 8월 한 달간 측정된 이산화탄소 농도(ppm)를 나타낸 자료이다. R 프로그램을 사용하여 이 데이터에 대해 도수분포표를 작성하고, 막대그래프로 시각화하시오. 또한 그래프를 통해 알 수 있는 특성을 서술하시오. (25점)
# 데이터 입력
co2 <- c(450, 520, 480, 550, 510, 610, 470, 530, 570, 500, 
         580, 610, 490, 550, 420, 580, 440, 520, 600, 560,
         410, 540, 590, 530, 490, 570, 610, 470, 540, 590, 510)

# 도수분포표 작성
freq_table <- table(cut(co2, breaks = seq(400, 650, by = 50)))
print(freq_table)
barplot(freq_table, main = "대기 중 이산화탄소 농도 분포", xlab = "이산화탄소 농도(ppm)", ylab = "빈도", names.arg = c("400-450", "450-500", "500-550", "550-600", "600-650"))